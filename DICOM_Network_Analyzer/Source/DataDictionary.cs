		public DataDictionary()
		{
		}
		public static CVRType FindVR(ushort g,ushort e)
		{
			CDictItem d=Dictionary.Find(g,e);
			if(d!=null)
				return d.vr1;

			else if(isPrivateCreator(g,e))
				return CVRType.LO;

			else
				return CVRType.UN;
		}
		public static string FindDescription(ushort g,ushort e)
		{
			CDictItem d=Dictionary.Find(g,e);
			if(d!=null)
				return d.title;
			else
				return "";
		}

		public static string FindVM(ushort g,ushort e)
		{
			CDictItem d=Dictionary.Find(g,e);
			if(d!=null)
				return d.vm;

			else if(isPrivateCreator(g,e))
				return "1";

			else
				return "1-n";
		}
		static bool isPrivateCreator(int group, int element)
		{
			return ((group & 1)==1 && element>=0x10 && element <=0xFF);
		}

		class CDictItem
		{
			public ushort group;
			public ushort element;
			public CVRType vr1;
			public string vm;
			public string title;

			public CDictItem( ushort g, ushort e, string r, string m, string t)
			{
				group=g;
				element=e;
				vr1=r;
				vm=m;
				title=t;
			}

		};
		public static int grel(ushort g, ushort e)
		{
			return (g<<16)+e;
		}
		class CDictionary :DictionaryBase 
		{
			public void Add(CDictItem d)
			{
				InnerHashtable.Add(grel(d.group,d.element),d);
			}
			public CDictItem Find(ushort g,ushort e)
			{
				return (CDictItem)InnerHashtable[grel(g,e)];
			}
		};
