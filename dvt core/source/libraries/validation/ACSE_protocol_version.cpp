bool ACSE_PROTOCOL_VERSION_CLASS::checkRange()

//  DESCRIPTION     : Check parameter range.
//  PRECONDITIONS   :
//  POSTCONDITIONS  :
//  EXCEPTIONS      : 
//  NOTES           :
//<<===========================================================================		
{
	bool result = true;
	
	// check parameter range
	if (atoi(valueM.c_str()) != PROTOCOL_VERSION)
	{
		addMessage(VAL_RULE_D_PARAM_3, "Value %s out of range", valueM.c_str());
		result = false;
	}
	
	// return result
	return result;
} 

